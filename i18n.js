const supportedLangs = ["ro", "ru", "en"];
const STORAGE_KEY = "nica_lang";

// –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Å—Ç–∞—Ä—Ç–æ–≤—ã–π —è–∑—ã–∫
function getInitialLanguage() {
  // 1. –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å —É–∂–µ —á—Ç–æ-—Ç–æ –≤—ã–±–∏—Ä–∞–ª ‚Äî —É–≤–∞–∂–∞–µ–º –µ–≥–æ –≤—ã–±–æ—Ä
  try {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved && supportedLangs.includes(saved)) {
      return saved;
    }
  } catch (e) {
    // localStorage –Ω–µ–¥–æ—Å—Ç—É–ø–µ–Ω ‚Äî –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º
  }

  // 2. –ë–µ—Ä—ë–º —è–∑—ã–∫ –±—Ä–∞—É–∑–µ—Ä–∞ / —É—Å—Ç—Ä–æ–π—Å—Ç–≤–∞
  let browserLang = "en";

  if (typeof navigator !== "undefined") {
    browserLang =
      (navigator.language || navigator.userLanguage || "en").toLowerCase();
    // –Ω–∞–ø—Ä–∏–º–µ—Ä "ru-RU" ‚Üí "ru"
    browserLang = browserLang.slice(0, 2);
  }

  // 3. –ï—Å–ª–∏ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º ‚Äî –∏—Å–ø–æ–ª—å–∑—É–µ–º –µ–≥–æ, –∏–Ω–∞—á–µ ‚Äî "en"
  if (supportedLangs.includes(browserLang)) {
    return browserLang;
  }

  return "en";
}

// –ü—Ä–∏–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–≤–æ–¥—ã –∫ —ç–ª–µ–º–µ–Ω—Ç–∞–º –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
function applyTranslations(lang) {
  const dict = translations[lang] || translations.en;

  // 1. –û–±–Ω–æ–≤–ª—è–µ–º –≤—Å–µ —ç–ª–µ–º–µ–Ω—Ç—ã —Å data-i18n
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  // 2. –û–±–Ω–æ–≤–ª—è–µ–º mailto-—Å—Å—ã–ª–∫–∏
  const mailto = document.getElementById("mailto");
  if (mailto && dict._mailto_subject && dict._mailto_body) {
    const subject = encodeURIComponent(dict._mailto_subject);
    const body = encodeURIComponent(dict._mailto_body);
    mailto.href = `mailto:webartoptimus@gmail.com?subject=${subject}&body=${body}`;
  }

  const ctaEmail = document.getElementById("cta-email");
  if (ctaEmail && dict._cta_subject && dict._cta_body) {
    const subject = encodeURIComponent(dict._cta_subject);
    const body = encodeURIComponent(dict._cta_body);
    ctaEmail.href = `mailto:webartoptimus@gmail.com?subject=${subject}&body=${body}`;
  }
}

